package com.championsita.name;

public class PersonajeConfig {
    public String texturaQuieto;
    public String sheetDerecha;
    public String sheetIzquierda;
    public String sheetArriba;
    public String sheetAbajo;
    public String sheetArribaDerecha;
    public String sheetArribaIzquierda;
    public String sheetAbajoDerecha;
    public String sheetAbajoIzquierda;

    public PersonajeConfig(String texturaQuieto, String sheetDerecha, String sheetIzquierda,
                           String sheetArriba, String sheetAbajo,
                           String sheetArribaDerecha, String sheetArribaIzquierda,
                           String sheetAbajoDerecha, String sheetAbajoIzquierda) {
        this.texturaQuieto = texturaQuieto;
        this.sheetDerecha = sheetDerecha;
        this.sheetIzquierda = sheetIzquierda;
        this.sheetArriba = sheetArriba;
        this.sheetAbajo = sheetAbajo;
        this.sheetArribaDerecha = sheetArribaDerecha;
        this.sheetArribaIzquierda = sheetArribaIzquierda;
        this.sheetAbajoDerecha = sheetAbajoDerecha;
        this.sheetAbajoIzquierda = sheetAbajoIzquierda;
    }
}
